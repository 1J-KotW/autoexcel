wb = openpyxl.Workbook()

# Create 5 sheets with 200 rows each
for sheet_num in range(1, 6):
    if sheet_num == 1:
        ws = wb.active
        ws.title = f"Лист{sheet_num}"
    else:
        ws = wb.create_sheet(f"Лист{sheet_num}")

    ws['A1'] = "Материал"
    ws['B1'] = "Единица измерения"
    ws['C1'] = "Количество"
    ws['D1'] = "Цена материала, за единицу"
    ws['E1'] = "Стоимость работ, за единицу"

    for row in range(2, 202):  # 200 rows
        mat, unit = random.choice(materials_list)
        qty = random.randint(1, 1000)
        ws.cell(row, 1).value = mat
        ws.cell(row, 2).value = unit
        ws.cell(row, 3).value = qty

        # Randomly pre-fill some prices (simulate manual entry)
        if random.random() < 0.1:  # 10% have pre-filled prices
            ws.cell(row, 4).value = round(random.uniform(10, 1000), 2)
            ws.cell(row, 5).value = round(random.uniform(5, 500), 2)

wb.save('large_test_smeta.xlsx')
